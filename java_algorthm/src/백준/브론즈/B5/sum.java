package 백준.브론즈.B5;

import java.util.Scanner;

public class sum {

    public static void main(String[] args) {

        // a + b 합계
        Scanner sc = new Scanner(System.in);

        System.out.println("a 입력해주세요");
        int a = sc.nextInt();

        System.out.println("b 입력해주세요");
        int b = sc.nextInt();

        int sum = a+b;
        System.out.println("합계는 = " + sum);


    }

}
